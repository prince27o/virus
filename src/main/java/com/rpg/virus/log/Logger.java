package com.rpg.virus.log;

/*
 * Class for printing logs in console
 */
public class Logger {


	/**
	 * Method to print message in new line of console
	 * 
	 * @param message
	 */
	public void info(String message) {
		System.out.println(message);
	}

	
	/**
	 * Method to print message in new line of console
	 * 
	 * @param message
	 */
	public void info(char character) {
		System.out.print(character );
	}

}
